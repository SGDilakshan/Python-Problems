# Access elements in a tuple using indexing.

tuple = ("Dilakshan", "cool", "dilli", "genuis")

print("First element:", tuple[0])
print("Second element:", tuple[1])
print("Third element:", tuple[2])
print("Fourth element:", tuple[3])
print()
print("First element:", tuple[-4])
print("Second element:", tuple[-3])
print("Third element:", tuple[-2])
print("Fourth element:", tuple[-1])